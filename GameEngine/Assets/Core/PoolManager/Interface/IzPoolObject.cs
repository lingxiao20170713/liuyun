using System;
using UnityEngine;

public interface IzPoolObject
{
    /// <summary>
    /// 获得对象池名称
    /// </summary>
    /// <returns></returns>
    string GetPoolName();

    /// <summary>
    /// 设置对象池名称
    /// </summary>
    void SetPoolName(string strPoolName);

    /// <summary>
    /// 获得资源名称
    /// </summary>
    /// <returns></returns>
    string GetPathName();

    /// <summary>
    /// 设置资源名称
    /// </summary>
    void SetPathName(string strPathName);

    /// <summary>
    /// 获得transform
    /// </summary>
    /// <returns></returns>
    Transform GetTransform();

    /// <summary>
    /// 获得gameObject
    /// </summary>
    /// <returns></returns>
    GameObject GetGameObject();
}